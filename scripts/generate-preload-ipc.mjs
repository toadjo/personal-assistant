/**
 * Generates `src/main/preload-ipc-literals.generated.ts` from `src/shared/ipc-channels.ts`
 * so preload can import string literals without importing the shared module at runtime.
 *
 * Usage: `node scripts/generate-preload-ipc.mjs` [--check]
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = join(root, "src", "shared", "ipc-channels.ts");
const outPath = join(root, "src", "main", "preload-ipc-literals.generated.ts");

function extractBlock(text, exportName) {
  const re = new RegExp(`export const ${exportName} = \\{([\\s\\S]*?)\\} as const`, "m");
  const m = text.match(re);
  if (!m) {
    throw new Error(`Could not find export const ${exportName} = { ... } as const in ${sourcePath}`);
  }
  return m[1].trim();
}

function formatObjectBody(body) {
  return body
    .split("\n")
    .map((l) => l.trim())
    .filter((l) => l.length > 0)
    .map((l) => `  ${l}`)
    .join("\n");
}

function buildOutput(invokeBody, pushBody) {
  return `/**
 * Generated by scripts/generate-preload-ipc.mjs — do not edit.
 * Source: src/shared/ipc-channels.ts
 */
export const invokeChannelMap = {
${formatObjectBody(invokeBody)}
} as const;

export const pushChannelMap = {
${formatObjectBody(pushBody)}
} as const;
`;
}

const src = readFileSync(sourcePath, "utf8");
const invokeBody = extractBlock(src, "IpcInvoke");
const pushBody = extractBlock(src, "IpcRendererEvent");
const output = buildOutput(invokeBody, pushBody);

const check = process.argv.includes("--check");
if (check) {
  let existing;
  try {
    existing = readFileSync(outPath, "utf8");
  } catch {
    console.error(`Missing ${outPath}; run: node scripts/generate-preload-ipc.mjs`);
    process.exit(1);
  }
  if (existing !== output) {
    console.error(`Generated preload IPC literals are out of date. Run: node scripts/generate-preload-ipc.mjs`);
    process.exit(1);
  }
  console.log("preload-ipc-literals.generated.ts is up to date.");
} else {
  writeFileSync(outPath, output, "utf8");
  console.log(`Wrote ${outPath}`);
}
